def fill_translations_with_fuzzy(filepath, lang):
    """Fill translations in a .po file, handling both empty and fuzzy entries."""
    if lang not in TRANSLATIONS:
        print(f"  SKIP: No translations defined for '{lang}'")
        return 0

    trans = TRANSLATIONS[lang]
    entries = parse_po_file(filepath)
    filled = 0

    for entry in entries:
        msgid = extract_msgid(entry['msgid_lines'])
        if msgid not in trans:
            continue

        msgstr = extract_msgstr(entry['msgstr_lines'])
        is_fuzzy = any('#, fuzzy' in c for c in entry['comments'])

        # Fill if empty or fuzzy-matched (incorrect translation)
        if msgstr == '' or is_fuzzy:
            new_msgstr = trans[msgid]
            entry['msgstr_lines'] = [f'msgstr "{new_msgstr}"']
            # Remove fuzzy flag
            entry['comments'] = [
                c for c in entry['comments'] if '#, fuzzy' not in c
            ]
            filled += 1

    if filled > 0:
        content = rebuild_po_file(entries)
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)

    return filled


def main():
    po_dir = os.path.dirname(os.path.abspath(__file__))
    languages = [
        'uk', 'de', 'fr', 'es', 'it', 'pl', 'cs', 'sk',
        'da', 'sv', 'nl', 'pt', 'be', 'kk', 'uz',
    ]

    print("Filling Session Recording Manager translations...")
    total_filled = 0
    for lang in languages:
        filepath = os.path.join(po_dir, f'{lang}.po')
        if not os.path.exists(filepath):
            print(f"  SKIP: {filepath} not found")
            continue
        filled = fill_translations_with_fuzzy(filepath, lang)
        total_filled += filled
        print(f"  {lang}: filled {filled} translations")

    print(f"\nTotal: {total_filled} translations filled across {len(languages)} languages")


if __name__ == '__main__':
    main()
